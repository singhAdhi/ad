// Instructions:- There are 7 days in a Week
// 0 Represents Sunday, 1 represents Monday and so on..
// Write a program that display which day it is using switch case.

// ======================================== Write your Code ========================= //
var day = 5;
switch (day) {
  case 0:
    console.log("sunday");
    break;
  case 1:
    console.log("monday");
    break;
  case 2:
    console.log("tuesday");
    break;
  case 3:
    console.log("wednesday");
    break;
  case 4:
    console.log("thursday");
    break;
  case 5:
    console.log("friday");
    break;
  case 6:
    console.log("saturday");
    break;
}
